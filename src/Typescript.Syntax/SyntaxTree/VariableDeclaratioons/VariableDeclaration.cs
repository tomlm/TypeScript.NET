namespace TypeScript.Syntax
{
    [NodeKindAttribute(NodeKind.VariableDeclaration)]
    public class VariableDeclaration : LocalVariableDeclaration
    {
        #region Properties
        public override NodeKind Kind
        {
            get { return NodeKind.VariableDeclaration; }
        }
        #endregion

        public override void AddChild(Node childNode)
        {
            base.AddChild(childNode);

            string nodeName = childNode.NodeName;
            switch (nodeName)
            {
                case "name":
                    this.Name = childNode;
                    break;

                case "type":
                    this.Type = childNode;
                    break;

                case "initializer":
                    this.Initializer = childNode;
                    break;

                default:
                    this.ProcessUnknownNode(childNode);
                    break;
            }
        }

        public void SetType(Node type, bool changeParent = true)
        {
            this.Type = type;
            if (changeParent && this.Type != null)
            {
                this.Type.Parent = this;
            }
        }
    }
}
